#!/usr/bin/env python3
"""Build the training dataset for the fraud/spam classifier.

Base corpus: alt-gnome/telegram-spam (HuggingFace) — ~21k real Russian Telegram
messages (ham = community chat, spam = job/investment recruitment scams).

We augment it for the deployment domain (news + finance channels):
  - SPAM  += financial phishing patterns underrepresented in the base corpus
            (account-blocked, fake prize, fake loan, sms-code social engineering)
  - HAM   += legitimate ADVERTISING and NEWS, so the model learns that ads and
            news are NOT fraud (the whole point: реклама ≠ скам).

Output: data/fraud_train.csv  (columns: label,text)
Run:    python scripts/build_dataset.py
"""

