def main() -> None:
    print("Downloading base corpus alt-gnome/telegram-spam ...")
    path = hf_hub_download(
        repo_id="alt-gnome/telegram-spam",
        filename="data/train-00000-of-00001.parquet",
        repo_type="dataset",
    )
    base = pd.read_parquet(path).dropna(subset=["text"])
    base["label"] = base["label"].astype(int)
    base = base[["text", "label"]]

    aug = pd.DataFrame(
        [{"text": t, "label": 1} for t in PHISHING]
        + [{"text": t, "label": 0} for t in LEGIT]
    )

    df = pd.concat([base, aug], ignore_index=True).drop_duplicates(subset=["text"])
    df = df.sample(frac=1.0, random_state=42).reset_index(drop=True)
    df.to_csv(OUT, index=False)

    print(f"Saved {len(df)} rows to {OUT}")
    print(df["label"].value_counts().to_string())


if __name__ == "__main__":
    sys.exit(main())
